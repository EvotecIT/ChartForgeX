namespace ChartForgeX.Typography;

/// <summary>
/// Defines horizontal text alignment inside a bounded layout region.
/// </summary>
public enum TextAlignment {
    /// <summary>Align text with the leading edge of the region.</summary>
    Left,

    /// <summary>Center text inside the region.</summary>
    Center,

    /// <summary>Align text with the trailing edge of the region.</summary>
    Right
}
